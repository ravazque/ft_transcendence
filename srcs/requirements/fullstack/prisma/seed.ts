//
// Also fills `tagged_info` with the default editorial texts / URLs
// consumed by the front end (header, hero, CTAs, modals, footer…),
// the FAQ list and the career milestones timeline.
//
// QA accounts (demo + testers) live in `test-users.ts` and are
// installed by `make test` so production-style seeds never touch
// user data.
//
// IMPORTANT: this seed is idempotent — uses `upsert` by (locale,
// slug), (moduleId, moduleLevel, locale) and (tag, locale), so it
// can be run again without duplicating rows.
//
// Run with:
//   make seed                  (host)
//   npx prisma db seed         (inside the fullstack container)

