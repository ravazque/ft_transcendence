// So that French has every key even when we have only translated the
// critical ones, we merge on top of the English content. English is
// the product language and is always complete. The admin replaces
// from Directus whatever is not translated.
function mergeWithFallback(
  primary: Array<{ tag: string; value: string }>,
  fallback: Array<{ tag: string; value: string }>,
): Array<{ tag: string; value: string }> {
  const map = new Map<string, string>()
  for (const f of fallback) map.set(f.tag, f.value)
  for (const p of primary) map.set(p.tag, p.value)
  return Array.from(map.entries()).map(([tag, value]) => ({ tag, value }))
}

